/**
 * Look up exact suburb-level coordinates.
 * Tries (suburb, postcode) first, then falls back to postcode-only match
 * for cases where the suburb name is unknown but postcode is recognised.
 *
 * @returns `{ lat, lng }` or `null` if suburb/postcode not in dataset.
 */
export function lookupSuburbCoords(
  suburb: string | null | undefined,
  postcode: string | null | undefined
): { lat: number; lng: number } | null {
  if (suburb && postcode) {
    const key = `${suburb.toLowerCase()}|${postcode}`;
    if (AU_SUBURB_COORDS[key]) return AU_SUBURB_COORDS[key];
  }
  // Postcode-only fallback: find the first entry whose postcode matches
  if (postcode) {
    const suffix = `|${postcode}`;
    for (const [k, v] of Object.entries(AU_SUBURB_COORDS)) {
      if (k.endsWith(suffix)) return v;
    }
  }
  return null;
}
